package petshop;
public class Agendamento {
    
    private String nomeDono, cpf, nomeAnimal, tipoAnimal, racaAnimal, servico, horario;

    public String getNomeDono() { return nomeDono; }
    public void setNomeDono(String nomeDono) { this.nomeDono = nomeDono; }

    public String getCpf() { return cpf; }
    public void setCpf(String cpf) { this.cpf = cpf; }

    public String getNomeAnimal() { return nomeAnimal; }
    public void setNomeAnimal(String nomeAnimal) { this.nomeAnimal = nomeAnimal; }

    public String getTipoAnimal() { return tipoAnimal; }
    public void setTipoAnimal(String tipoAnimal) { this.tipoAnimal = tipoAnimal; }

    public String getRacaAnimal() { return racaAnimal; }
    public void setRacaAnimal(String racaAnimal) { this.racaAnimal = racaAnimal; }

    public String getServico() { return servico; }
    public void setServico(String servico) { this.servico = servico; }

    public String getHorario() { return horario; }
    public void setHorario(String horario) { this.horario = horario; }
}